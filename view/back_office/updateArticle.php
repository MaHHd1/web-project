<?php
include '../../controller/ArticleController.php';

$articleController = new ArticleController();
$error = "";

// Vérifiez si l'ID est défini pour récupérer les données existantes
if (isset($_POST['id'])) {
    $article = $articleController->showArticle($_POST['id']);
}

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Vérification des champs requis
    if (
        !empty($_POST["titre"]) &&
        !empty($_POST["contenu_txt"]) &&
        !empty($_POST["date_creation"])
    ) {
        // Gestion du fichier image
        $imageData = null;
        if (!empty($_FILES['contenu_img']['tmp_name'])) {
            $imageData = file_get_contents($_FILES['contenu_img']['tmp_name']);
        } elseif (isset($article['contenu_img'])) {
            // Conserver l'image existante si aucun fichier n'est uploadé
            $imageData = $article['contenu_img'];
        }

        try {
            // Préparer les données pour la mise à jour
            $updatedArticle = [
                'id' => $_POST['id'],
                'titre' => $_POST['titre'],
                'contenu_txt' => $_POST['contenu_txt'],
                'contenu_img' => $imageData,
                'date_creation' => $_POST['date_creation']
            ];

            // Mise à jour de l'article
            $articleController->updateArticle($updatedArticle, $_POST['id']);
            header('Location:tables.php');
        } catch (Exception $e) {
            $error = "Erreur lors de la mise à jour : " . $e->getMessage();
        }
    } else {
        $error = "Tous les champs doivent être remplis.";
    }
}
